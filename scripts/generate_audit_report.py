# ━━ Helper: create standard table ━━
def make_table(headers, rows, col_ratios=None):
    available_w = CONTENT_W
    if col_ratios is None:
        num_cols = len(headers)
        col_ratios = [1.0 / num_cols] * num_cols
    col_widths = [r * available_w for r in col_ratios]

    header_row = [Paragraph(ar(h), style_table_header) for h in headers]
    data = [header_row]
    for row in rows:
        data_row = []
        for cell in row:
            if isinstance(cell, Paragraph):
                data_row.append(cell)
            elif isinstance(cell, str):
                data_row.append(Paragraph(ar(cell), style_table_cell))
            else:
                data_row.append(Paragraph(str(cell), style_table_cell))
        data.append(data_row)

    table = Table(data, colWidths=col_widths, hAlign='CENTER')
    style_cmds = [
        ('BACKGROUND', (0, 0), (-1, 0), TABLE_HEADER_COLOR),
        ('TEXTCOLOR', (0, 0), (-1, 0), TABLE_HEADER_TEXT),
        ('GRID', (0, 0), (-1, -1), 0.5, BORDER_COLOR),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('LEFTPADDING', (0, 0), (-1, -1), 8),
        ('RIGHTPADDING', (0, 0), (-1, -1), 8),
        ('TOPPADDING', (0, 0), (-1, -1), 6),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
    ]
    for i in range(1, len(data)):
        if i % 2 == 0:
            style_cmds.append(('BACKGROUND', (0, i), (-1, i), TABLE_ROW_ODD))
        else:
            style_cmds.append(('BACKGROUND', (0, i), (-1, i), TABLE_ROW_EVEN))
    table.setStyle(TableStyle(style_cmds))
    return table

# ━━ Helper: bullet point ━━
def bullet_item(text, symbol='\u25cf'):
    ar_text = ar(text)
    return Paragraph(f'{ar_text} {symbol}', style_bullet)

# ━━ Page background callback ━━
def draw_page_bg(canvas, doc):
    canvas.saveState()
    canvas.setFillColor(BG_PAGE)
    canvas.rect(0, 0, PAGE_W, PAGE_H, fill=1, stroke=0)
    # Top accent line
    canvas.setStrokeColor(ACCENT)
    canvas.setLineWidth(2)
    canvas.line(LEFT_MARGIN, PAGE_H - TOP_MARGIN + 10, PAGE_W - RIGHT_MARGIN, PAGE_H - TOP_MARGIN + 10)
    # Footer
    canvas.setFont('Amiri', 8)
    canvas.setFillColor(TEXT_MUTED)
    footer_text = ar('Vixor V2 - تقرير التدقيق التقني الشامل')
    canvas.drawCentredString(PAGE_W / 2, 25, footer_text)
    # Page number
    canvas.setFont('LiberationSerif', 8)
    canvas.setFillColor(TEXT_MUTED)
    canvas.drawCentredString(PAGE_W / 2, 15, str(doc.page))
    canvas.restoreState()

def draw_first_page(canvas, doc):
    draw_page_bg(canvas, doc)

