# ━━ Arabic Text Helper ━━
def ar(text):
    """Reshape and reorder Arabic text for proper RTL rendering in ReportLab."""
    reshaped = arabic_reshaper.reshape(text)
    bidi_text = get_display(reshaped)
    return bidi_text

def ar_para(text, style):
    """Create a Paragraph with properly reshaped Arabic text."""
    return Paragraph(ar(text), style)

